

import requests

from . import utils

from .accountaccess import AccountAccess
from .accounts import Accounts
from .balances import Balances
from .beneficiaries import Beneficiaries
from .parties import Parties
from .sandbox import Sandbox
from .scheduledpayments import ScheduledPayments
from .standingorders import StandingOrders
from .statements import Statements
from .transactions import Transactions


SERVERS = [
	"https://apis.nbg.gr/sandbox/uk.openbanking.accountinfo/oauth2/v3.1.5",
	"https://services.nbg.gr/apis/open-banking/v3.1.5/aisp",
]


class SDK:
    
    account_access: AccountAccess
    accounts: Accounts
    balances: Balances
    beneficiaries: Beneficiaries
    parties: Parties
    sandbox: Sandbox
    scheduled_payments: ScheduledPayments
    standing_orders: StandingOrders
    statements: Statements
    transactions: Transactions

    _client: requests.Session
    _security_client: requests.Session
    
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.account_access = AccountAccess(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.accounts = Accounts(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.balances = Balances(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.beneficiaries = Beneficiaries(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.parties = Parties(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.sandbox = Sandbox(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.scheduled_payments = ScheduledPayments(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.standing_orders = StandingOrders(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.statements = Statements(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.transactions = Transactions(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    