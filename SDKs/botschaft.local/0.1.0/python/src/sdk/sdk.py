

import requests
from typing import Any,Optional
from sdk.models import shared, operations
from . import utils


from .discord import Discord
from .slack import Slack
from .sns import Sns
from .twilio import Twilio


SERVERS = [
	"http://botschaft.local",
]


class SDK:
    
    discord: Discord
    slack: Slack
    sns: Sns
    twilio: Twilio

    _client: requests.Session
    _security_client: requests.Session
    
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        
    

    def config_client(self, client: requests.Session):
        self._client = client
        
    
    
    
    def config_config_get(self, request: operations.ConfigConfigGetRequest) -> operations.ConfigConfigGetResponse:
        r"""Config
        """
        
        base_url = self._server_url
        
        url = base_url.removesuffix("/") + "/config"
        
        headers = utils.get_headers(request.headers)
        
        client = self._client
        
        r = client.request("GET", url, headers=headers)
        content_type = r.headers.get("Content-Type")

        res = operations.ConfigConfigGetResponse(status_code=r.status_code, content_type=content_type)
        
        if r.status_code == 200:
            if utils.match_content_type(content_type, "application/json"):
                out = utils.unmarshal_json(r.text, Optional[shared.Config])
                res.config = out
        elif r.status_code == 422:
            if utils.match_content_type(content_type, "application/json"):
                out = utils.unmarshal_json(r.text, Optional[shared.HTTPValidationError])
                res.http_validation_error = out

        return res

    
    def topic_topic_topic_name_get(self, request: operations.TopicTopicTopicNameGetRequest) -> operations.TopicTopicTopicNameGetResponse:
        r"""Topic
        """
        
        base_url = self._server_url
        
        url = utils.generate_url(base_url, "/topic/{topic_name}", request.path_params)
        
        headers = utils.get_headers(request.headers)
        query_params = utils.get_query_params(request.query_params)
        
        client = self._client
        
        r = client.request("GET", url, params=query_params, headers=headers)
        content_type = r.headers.get("Content-Type")

        res = operations.TopicTopicTopicNameGetResponse(status_code=r.status_code, content_type=content_type)
        
        if r.status_code == 200:
            if utils.match_content_type(content_type, "application/json"):
                out = utils.unmarshal_json(r.text, Optional[Any])
                res.topic_topic_topic_name_get_200_application_json_any = out
        elif r.status_code == 422:
            if utils.match_content_type(content_type, "application/json"):
                out = utils.unmarshal_json(r.text, Optional[shared.HTTPValidationError])
                res.http_validation_error = out

        return res

    