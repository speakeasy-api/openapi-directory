

import requests

from . import utils

from .fulfillment_policy import FulfillmentPolicy
from .kyc import Kyc
from .onboarding import Onboarding
from .payment_policy import PaymentPolicy
from .payments_program import PaymentsProgram
from .privilege import Privilege
from .program import Program
from .rate_table import RateTable
from .return_policy import ReturnPolicy
from .sales_tax import SalesTax


SERVERS = [
	"https://api.ebay.com{basePath}",
]


class SDK:
    
    fulfillment_policy: FulfillmentPolicy
    kyc: Kyc
    onboarding: Onboarding
    payment_policy: PaymentPolicy
    payments_program: PaymentsProgram
    privilege: Privilege
    program: Program
    rate_table: RateTable
    return_policy: ReturnPolicy
    sales_tax: SalesTax

    _client: requests.Session
    _security_client: requests.Session
    
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.fulfillment_policy = FulfillmentPolicy(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.kyc = Kyc(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.onboarding = Onboarding(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.payment_policy = PaymentPolicy(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.payments_program = PaymentsProgram(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.privilege = Privilege(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.program = Program(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.rate_table = RateTable(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.return_policy = ReturnPolicy(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.sales_tax = SalesTax(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    