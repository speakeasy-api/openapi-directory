

import requests

from . import utils

from .cm_facing import CmFacing
from .consent_flow import ConsentFlow
from .data_flow import DataFlow
from .discovery import Discovery
from .hip_facing import HipFacing
from .hiu_facing import HiuFacing
from .identification import Identification
from .link import Link
from .monitoring import Monitoring
from .patient_notification import PatientNotification
from .profile import Profile
from .services import Services
from .sessions import Sessions
from .subscriptions import Subscriptions
from .user_auth import UserAuth


SERVERS = [
	"https://dev.ndhm.gov.in/gateway",
]


class SDK:
    
    cm_facing: CmFacing
    consent_flow: ConsentFlow
    data_flow: DataFlow
    discovery: Discovery
    hip_facing: HipFacing
    hiu_facing: HiuFacing
    identification: Identification
    link: Link
    monitoring: Monitoring
    patient_notification: PatientNotification
    profile: Profile
    services: Services
    sessions: Sessions
    subscriptions: Subscriptions
    user_auth: UserAuth

    _client: requests.Session
    _security_client: requests.Session
    
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.cm_facing = CmFacing(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.consent_flow = ConsentFlow(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.data_flow = DataFlow(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.discovery = Discovery(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.hip_facing = HipFacing(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.hiu_facing = HiuFacing(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.identification = Identification(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.link = Link(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.monitoring = Monitoring(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.patient_notification = PatientNotification(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.profile = Profile(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.services = Services(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.sessions = Sessions(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.subscriptions = Subscriptions(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.user_auth = UserAuth(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    