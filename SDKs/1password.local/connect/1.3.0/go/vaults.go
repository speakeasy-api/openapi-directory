type Vaults struct {
	_defaultClient  HTTPClient
	_securityClient HTTPClient
	_serverURL      string
	_language       string
	_sdkVersion     string
	_genVersion     string
}

func NewVaults(defaultClient, securityClient HTTPClient, serverURL, language, sdkVersion, genVersion string) *Vaults {
	return &Vaults{
		_defaultClient:  defaultClient,
		_securityClient: securityClient,
		_serverURL:      serverURL,
		_language:       language,
		_sdkVersion:     sdkVersion,
		_genVersion:     genVersion,
	}
}

// GetVaultByID - Get Vault details and metadata
