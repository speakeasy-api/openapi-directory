func NewCam(defaultClient, securityClient HTTPClient, serverURL, language, sdkVersion, genVersion string) *Cam {
	return &Cam{
		_defaultClient:  defaultClient,
		_securityClient: securityClient,
		_serverURL:      serverURL,
		_language:       language,
		_sdkVersion:     sdkVersion,
		_genVersion:     genVersion,
	}
}

