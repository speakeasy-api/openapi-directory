

import requests

from . import utils

from .agr import Agr
from .annotation import Annotation
from .chromosome import Chromosome
from .gene import Gene
from .lookup import Lookup
from .map import Map
from .ontology import Ontology
from .pathway import Pathway
from .qtl import Qtl
from .quantitative_phenotype import QuantitativePhenotype
from .rat_strain import RatStrain
from .sslp import Sslp
from .statistics import Statistics
from .enrichment_web_service import EnrichmentWebService


SERVERS = [
	"https://mcw.edu//rest.rgd.mcw.edu/rgdws",
]


class SDK:
    
    agr: Agr
    annotation: Annotation
    chromosome: Chromosome
    gene: Gene
    lookup: Lookup
    map: Map
    ontology: Ontology
    pathway: Pathway
    qtl: Qtl
    quantitative_phenotype: QuantitativePhenotype
    rat_strain: RatStrain
    sslp: Sslp
    statistics: Statistics
    enrichment_web_service: EnrichmentWebService

    _client: requests.Session
    _security_client: requests.Session
    
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.agr = Agr(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.annotation = Annotation(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.chromosome = Chromosome(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.gene = Gene(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.lookup = Lookup(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.map = Map(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.ontology = Ontology(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.pathway = Pathway(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.qtl = Qtl(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.quantitative_phenotype = QuantitativePhenotype(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.rat_strain = RatStrain(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.sslp = Sslp(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.statistics = Statistics(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.enrichment_web_service = EnrichmentWebService(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    