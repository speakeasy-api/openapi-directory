

import requests

from . import utils

from .api_info import APIInfo
from .authentication import Authentication
from .container_groups import ContainerGroups
from .file_shares import FileShares
from .images import Images
from .private_docker_images_registry import PrivateDockerImagesRegistry
from .public_ip_addresses import PublicIPAddresses
from .quota import Quota
from .single_containers import SingleContainers
from .volumes import Volumes


SERVERS = [
	"https://containers-api.ng.bluemix.net/v3",
]


class SDK:
    
    api_info: APIInfo
    authentication: Authentication
    container_groups: ContainerGroups
    file_shares: FileShares
    images: Images
    private_docker_images_registry: PrivateDockerImagesRegistry
    public_ip_addresses: PublicIPAddresses
    quota: Quota
    single_containers: SingleContainers
    volumes: Volumes

    _client: requests.Session
    _security_client: requests.Session
    
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.api_info = APIInfo(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.authentication = Authentication(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.container_groups = ContainerGroups(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.file_shares = FileShares(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.images = Images(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.private_docker_images_registry = PrivateDockerImagesRegistry(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.public_ip_addresses = PublicIPAddresses(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.quota = Quota(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.single_containers = SingleContainers(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.volumes = Volumes(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    