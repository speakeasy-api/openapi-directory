    public ChatV2ServiceRole withSid(String sid) {
        this.sid = sid;
        return this;
    }
    @JsonInclude(Include.NON_ABSENT)
    @JsonProperty("type")
    public RoleEnumRoleTypeEnum type;
    public ChatV2ServiceRole withType(RoleEnumRoleTypeEnum type) {
        this.type = type;
        return this;
    }
