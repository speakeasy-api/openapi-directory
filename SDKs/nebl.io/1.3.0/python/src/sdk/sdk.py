

import requests

from . import utils

from .insight import Insight
from .json_rpc import JSONRPC
from .ntp1 import Ntp1
from .testnet_faucet import TestnetFaucet
from .testnet_insight import TestnetInsight
from .testnet_ntp1 import TestnetNtp1


SERVERS = [
	"https://ntp1node.nebl.io/",
]


class SDK:
    
    insight: Insight
    json_rpc: JSONRPC
    ntp1: Ntp1
    testnet_faucet: TestnetFaucet
    testnet_insight: TestnetInsight
    testnet_ntp1: TestnetNtp1

    _client: requests.Session
    _security_client: requests.Session
    
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.insight = Insight(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.json_rpc = JSONRPC(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.ntp1 = Ntp1(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.testnet_faucet = TestnetFaucet(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.testnet_insight = TestnetInsight(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.testnet_ntp1 = TestnetNtp1(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    