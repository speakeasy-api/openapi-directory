package openapisdk.models.shared;



/**
 * Setting
 * A setting for Glass.
**/
public class Setting {
    public String id;
    public Setting withId(String id) {
        this.id = id;
        return this;
    }
    public String kind;
    public Setting withKind(String kind) {
        this.kind = kind;
        return this;
    }
    public String value;
    public Setting withValue(String value) {
        this.value = value;
        return this;
    }
}