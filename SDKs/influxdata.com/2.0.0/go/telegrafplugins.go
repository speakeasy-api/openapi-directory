func NewTelegrafPlugins(defaultClient, securityClient HTTPClient, serverURL, language, sdkVersion, genVersion string) *TelegrafPlugins {
	return &TelegrafPlugins{
		_defaultClient:  defaultClient,
		_securityClient: securityClient,
		_serverURL:      serverURL,
		_language:       language,
		_sdkVersion:     sdkVersion,
		_genVersion:     genVersion,
	}
}

