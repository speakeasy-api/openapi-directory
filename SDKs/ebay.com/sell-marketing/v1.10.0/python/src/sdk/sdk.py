

import requests

from . import utils

from .ad import Ad
from .ad_report import AdReport
from .ad_report_metadata import AdReportMetadata
from .ad_report_task import AdReportTask
from .campaign import Campaign
from .item_price_markdown import ItemPriceMarkdown
from .item_promotion import ItemPromotion
from .promotion import Promotion
from .promotion_report import PromotionReport
from .promotion_summary_report import PromotionSummaryReport


SERVERS = [
	"https://api.ebay.com{basePath}",
]


class SDK:
    
    ad: Ad
    ad_report: AdReport
    ad_report_metadata: AdReportMetadata
    ad_report_task: AdReportTask
    campaign: Campaign
    item_price_markdown: ItemPriceMarkdown
    item_promotion: ItemPromotion
    promotion: Promotion
    promotion_report: PromotionReport
    promotion_summary_report: PromotionSummaryReport

    _client: requests.Session
    _security_client: requests.Session
    
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.ad = Ad(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.ad_report = AdReport(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.ad_report_metadata = AdReportMetadata(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.ad_report_task = AdReportTask(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.campaign = Campaign(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.item_price_markdown = ItemPriceMarkdown(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.item_promotion = ItemPromotion(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.promotion = Promotion(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.promotion_report = PromotionReport(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.promotion_summary_report = PromotionSummaryReport(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    