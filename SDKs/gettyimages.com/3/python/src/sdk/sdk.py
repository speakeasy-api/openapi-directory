SERVERS = [
	"https://gettyimages.com",
]


class SDK:
    
    affiliate_search: AffiliateSearch
    artists: Artists
    asset_changes: AssetChanges
    asset_licensing: AssetLicensing
    boards: Boards
    collections: Collections
    countries: Countries
