SERVERS = [
	"https://tracking.api.here.com/",
]


class SDK:
    
    aliases: Aliases
    associations: Associations
    c2_c: C2C
    device_associations: DeviceAssociations
    events: Events
    geofence: Geofence
    geofence_associations: GeofenceAssociations
    ingestion: Ingestion
    locations: Locations
    metadata: Metadata
    notifications: Notifications
