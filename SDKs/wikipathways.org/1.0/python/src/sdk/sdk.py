

import requests

from . import utils

from .all_functions import AllFunctions
from .curation_tags import CurationTags
from .download import Download
from .history import History
from .ontology_tags import OntologyTags
from .organism_list import OrganismList
from .pathway_information import PathwayInformation
from .pathway_list import PathwayList
from .search import Search
from .user_management import UserManagement
from .write_create_update_delete_ import WriteCreateUpdateDelete


SERVERS = [
	"https://webservice.wikipathways.org",
]


class SDK:
    
    all_functions: AllFunctions
    curation_tags: CurationTags
    download: Download
    history: History
    ontology_tags: OntologyTags
    organism_list: OrganismList
    pathway_information: PathwayInformation
    pathway_list: PathwayList
    search: Search
    user_management: UserManagement
    write_create_update_delete_: WriteCreateUpdateDelete

    _client: requests.Session
    _security_client: requests.Session
    
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.all_functions = AllFunctions(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.curation_tags = CurationTags(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.download = Download(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.history = History(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.ontology_tags = OntologyTags(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.organism_list = OrganismList(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.pathway_information = PathwayInformation(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.pathway_list = PathwayList(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.search = Search(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.user_management = UserManagement(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.write_create_update_delete_ = WriteCreateUpdateDelete(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    