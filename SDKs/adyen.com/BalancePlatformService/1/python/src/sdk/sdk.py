

import requests

from . import utils

from .account_holders import AccountHolders
from .balance_accounts import BalanceAccounts
from .documents import Documents
from .general import General
from .legal_entities import LegalEntities
from .payment_instrument_groups import PaymentInstrumentGroups
from .payment_instruments import PaymentInstruments
from .transaction_rules import TransactionRules
from .transfer_instruments import TransferInstruments


SERVERS = [
	"https://balanceplatform-api-test.adyen.com/bcl/v1",
]


class SDK:
    
    account_holders: AccountHolders
    balance_accounts: BalanceAccounts
    documents: Documents
    general: General
    legal_entities: LegalEntities
    payment_instrument_groups: PaymentInstrumentGroups
    payment_instruments: PaymentInstruments
    transaction_rules: TransactionRules
    transfer_instruments: TransferInstruments

    _client: requests.Session
    _security_client: requests.Session
    
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.account_holders = AccountHolders(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.balance_accounts = BalanceAccounts(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.documents = Documents(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.general = General(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.legal_entities = LegalEntities(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.payment_instrument_groups = PaymentInstrumentGroups(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.payment_instruments = PaymentInstruments(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.transaction_rules = TransactionRules(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.transfer_instruments = TransferInstruments(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    