

import requests
from sdk.models import shared
from . import utils

from .account_and_portfolio import AccountAndPortfolio
from .financial_instrument_definitions import FinancialInstrumentDefinitions
from .market_data import MarketData
from .oauth import OAuth
from .order_margin_requirements import OrderMarginRequirements
from .orders import Orders
from .trades import Trades


SERVERS = [
	"https://www.interactivebrokers.com/tradingapi/v1",
]


class SDK:
    
    account_and_portfolio: AccountAndPortfolio
    financial_instrument_definitions: FinancialInstrumentDefinitions
    market_data: MarketData
    o_auth: OAuth
    order_margin_requirements: OrderMarginRequirements
    orders: Orders
    trades: Trades

    _client: requests.Session
    _security_client: requests.Session
    _security: shared.Security
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        
        if self._security is not None:
            self._security_client = utils.configure_security_client(self._client, self._security)
        self._init_sdks()
    

    def config_security(self, security: shared.Security):
        self._security = security
        self._security_client = utils.configure_security_client(self._client, security)
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.account_and_portfolio = AccountAndPortfolio(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.financial_instrument_definitions = FinancialInstrumentDefinitions(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.market_data = MarketData(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.o_auth = OAuth(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.order_margin_requirements = OrderMarginRequirements(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.orders = Orders(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.trades = Trades(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    