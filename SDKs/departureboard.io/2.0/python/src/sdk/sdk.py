

import requests

from . import utils

from .departures_and_arrivals import DeparturesAndArrivals
from .fastest_and_next_departures import FastestAndNextDepartures
from .service_information import ServiceInformation


SERVERS = [
	"https://api.departureboard.io/api/v2.0",
]


class SDK:
    
    departures_and_arrivals: DeparturesAndArrivals
    fastest_and_next_departures: FastestAndNextDepartures
    service_information: ServiceInformation

    _client: requests.Session
    _security_client: requests.Session
    
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.departures_and_arrivals = DeparturesAndArrivals(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.fastest_and_next_departures = FastestAndNextDepartures(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.service_information = ServiceInformation(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    