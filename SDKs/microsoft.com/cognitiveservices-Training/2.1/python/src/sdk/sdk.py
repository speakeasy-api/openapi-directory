

import requests

from . import utils

from .domainsapi import DomainsAPI
from .imageapi import ImageAPI
from .imageregionproposalapi import ImageRegionProposalAPI
from .predictionsapi import PredictionsAPI
from .projectapi import ProjectAPI
from .tagsapi import TagsAPI


SERVERS = [
	"https://southcentralus.api.cognitive.microsoft.com/customvision/v2.1/Training",
]


class SDK:
    
    domains_api: DomainsAPI
    image_api: ImageAPI
    image_region_proposal_api: ImageRegionProposalAPI
    predictions_api: PredictionsAPI
    project_api: ProjectAPI
    tags_api: TagsAPI

    _client: requests.Session
    _security_client: requests.Session
    
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.domains_api = DomainsAPI(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.image_api = ImageAPI(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.image_region_proposal_api = ImageRegionProposalAPI(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.predictions_api = PredictionsAPI(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.project_api = ProjectAPI(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.tags_api = TagsAPI(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    