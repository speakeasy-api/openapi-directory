func NewPathway(defaultClient, securityClient HTTPClient, serverURL, language, sdkVersion, genVersion string) *Pathway {
	return &Pathway{
		_defaultClient:  defaultClient,
		_securityClient: securityClient,
		_serverURL:      serverURL,
		_language:       language,
		_sdkVersion:     sdkVersion,
		_genVersion:     genVersion,
	}
}

