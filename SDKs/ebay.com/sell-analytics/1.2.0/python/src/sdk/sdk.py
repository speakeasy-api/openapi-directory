

import requests

from . import utils

from .customer_service_metric import CustomerServiceMetric
from .seller_standards_profile import SellerStandardsProfile
from .traffic_report import TrafficReport


SERVERS = [
	"https://api.ebay.com{basePath}",
]


class SDK:
    
    customer_service_metric: CustomerServiceMetric
    seller_standards_profile: SellerStandardsProfile
    traffic_report: TrafficReport

    _client: requests.Session
    _security_client: requests.Session
    
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "0.0.1"
    _gen_version: str = "internal"

    def __init__(self) -> None:
        self._client = requests.Session()
        self._security_client = requests.Session()
        self._init_sdks()


    def config_server_url(self, server_url: str, params: dict[str, str]):
        if params is not None:
            self._server_url = utils.replace_parameters(server_url, params)
        else:
            self._server_url = server_url

        self._init_sdks()
    

    def config_client(self, client: requests.Session):
        self._client = client
        self._init_sdks()
    
    
    def _init_sdks(self):
        
        self.customer_service_metric = CustomerServiceMetric(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.seller_standards_profile = SellerStandardsProfile(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.traffic_report = TrafficReport(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
    
    